class DeviceIDGenerator:
    '''
    Generates device ID for the request.
    '''
    @staticmethod
    def generate_device_id():
        return '-'.join([''.join(random.choices(string.ascii_lowercase + string.digits, k=part_length))
                         for part_length in [8, 4, 4, 4, 12]])
        

